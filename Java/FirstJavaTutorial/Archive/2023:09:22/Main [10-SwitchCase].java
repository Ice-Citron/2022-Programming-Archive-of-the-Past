
public class Main {
    public static void main(String[] args){

        String day = "sa";

        switch(day){

            case "Ahad":
                System.out.println("Hari ahad");
                break;
            case "Isnin":
                System.out.println("Hari Isnin");
                break;
            case "Selasa":
                System.out.println("Hari Selasa");
                break;
            case "Rabu":
                System.out.println("Hari Rabu");
                break;
            case "Khamis":
                System.out.println("Hari Khamis");
                break;
            case "Jumaat":
                System.out.println("Hari Jumaat");
                break;
            case "Sabtu":
                System.out.println("Hari Sabtu");
                break;
            default:
                System.out.println("Jawapan tidak valid");
        }


    }
}
